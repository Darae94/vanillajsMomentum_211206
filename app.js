const calculator = {
    plus: function(a, b) {
        console.log(a + b);
    },
    minus: function(a, b) {
        console.log(a - b);
    },
    times: function(a, b) {
        console.log(a * b);
    },
    divide: function(a, b) {
        console.log(a / b);
    },
    power: function(a, b) {
        console.log(a ** b);
    },
}

calculator.plus(8, 5);
calculator.minus(8, 5);
calculator.times(8, 5);
calculator.divide(8, 5);
calculator.power(8, 5);